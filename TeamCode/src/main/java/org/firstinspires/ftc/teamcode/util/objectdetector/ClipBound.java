package org.firstinspires.ftc.teamcode.util.objectdetector;

public class ClipBound {
    int left;
    int top;
    int width;
    int height;

    public ClipBound(int left, int top, int width, int height) {
        this.left = left;
        this.top = top;
        this.width = width;
        this.height = height;
    }
}
